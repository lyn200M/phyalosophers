#include "utils.h"

bool	is_all_number(char **av)
{
	int	i;
	int	j;

	i = 1;
	while (av[i])
	{
		j = 0;
		while (av[i][j])
		{
			if (!(av[i][j] >= '0' && av[i][j] <= '9'))
				return (false);
			j++;
		}
		i++;
	}
	return (true);
}
